exports.renderPhotoSetPage = (req, res) => {
    let photos;
    switch (req.params.category) {
        case "portraits":
            photos = getPortraitPictures(req.params.imageset);
            break;
        case "newborns":
            photos = getNewbornPictures(req.params.imageset);
            break;
        case "kidsandfamilies":
            photos = getKidsAndFamiliesPictures(req.params.imageset);
            break;
        case "couples":
            photos = getCouplesPictures(req.params.imageset);
            break;
        case "maternity":
            photos = getMaternityPictures(req.params.imageset);
            break;
    }

    res.render("photoSet", {
        layout: "main",
        language: req.params.language,
        isEnglish: req.params.language === "en",
        isPortuguese: req.params.language === "pt",
        photos
    });
};

